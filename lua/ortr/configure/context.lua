require('treesitter-context').setup{
  enable = true,
  multiwindow = false,

  -- В C++ заголовки бывают длинными, лучше ограничить до 5 строк,
  -- чтобы контекст не занял пол-экрана.
  max_lines = 5,

  min_window_height = 0,
  line_numbers = true,
  multiline_threshold = 20,

  -- 'outer' полезнее: если контекст слишком большой, он оставит
  -- заголовок функции/класса, но скроет вложенные if/while.
  trim_scope = 'outer',

  -- 'cursor' — самый адекватный режим. Контекст меняется в зависимости от того,
  -- где твой курсор, а не просто верхняя видимая строка.
  mode = 'cursor',

  -- Визуальный разделитель. Очень рекомендую поставить тонкий символ,
  -- чтобы сразу видеть, где заканчивается "липкий" блок и начинается код.
  separator = '─',

  zindex = 20,
  on_attach = nil,
}
