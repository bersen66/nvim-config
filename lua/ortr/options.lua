vim.cmd('set smarttab') -- <Tab> in front of line inserts blanks 'shiftwidth' times.
vim.cmd('set mouse=a') -- enable mouse in all modes
vim.cmd('set encoding=utf-8') -- string encoding for RPC communication

vim.cmd('set number') -- Display line number
vim.cmd('set relativenumber') -- Line numbers relative to cursor

vim.cmd('set tabstop=4') -- tab size
vim.cmd('set shiftwidth=4') -- shift with << and >>
vim.cmd('set softtabstop=4') -- number of spaces that a <Tab> counts for while performing editing operations  
vim.cmd('set expandtab') -- tab is spaces
vim.cmd('set autoindent') -- copy blanks from previous line
